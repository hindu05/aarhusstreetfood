	<p>Alle boder tager imod kontanter. Derudover tager alle boder også enten kreditkort eller mobilepay.</p>
	<b>Glemte sager:</b>
	<p>Åhhh nej! Nu var det lige så hyggeligt i går, og så vågner du i dag med en dundrende hovedpine, men desværre uden solbriller, jakke, taske og værdighed.<br>
	Værdigheden kan vi ikke hjælpe dig med, men de glemte sager kan du måske have glemt ved os. Henvend dig i vores hvide mainbar, og så skal vi se efter, om du har glemt dine sager her!</p>
	<b>Transport:</b>
	<p>Det tager 4 minutter at gå hertil fra Aarhus Hovedbanegård
	<br>Det tager 11 minutter at gå hertil fra Aarhus Domkirke
	<br>Det tager 10 minutter at gå hertil fra Aros
	<br>Det tager 9 minutter at cykle hertil fra Marselisborg Lystbådehavn
	<br>Det tager 8 minutter at cykle hertil fra Aarhus Lystbådehavn</p>
	<p>Vi ligger lige ved Aarhus Rutebilsstation, så du kan næsten altid finde en bus, der stopper her.</p>
	<b>Parkering:</b>
