		<article class="col-md-6 col-lg-6">
			<p>Dét skal vi også have i Aarhus.</p>
			<p>Det var tanken, da Thomas og Britt besøgte Borough Market i London!</p>
			<p>Og så gik den vilde jagt efter en indendørs lokation - for det bliver altså kedeligt at spise lækker mad, hvis man skal stå i regnvejr, og derfor var det nødvendigt med tag over hovedet.
		</p>
			<p>Stille og roligt fik de kontakt til en masse mennesker, der havde spændende ideer til veltillavet, ærlig mad, men stedet manglede stadig…</p>
			<p>Indtil en dag hvor den gamle garage ved Aarhus Rutebilsstation åbenbarede sig (indsæt selv lyden af englesang). Garagens rå vægge, det store ovenlysvindue og en fremragende placering.  
		</p>
			<p>Aarhus-Iværksætteren Kasper Kragelund kom med på projektet, og så begyndte arbejdet med at gøre Garagen klar til, at duften af mad for alvor kunne brede sig.</p>
			<p>Der blev stiftet en stadeforening, så madboderne i fællesskab kan tage beslutninger om udsmykning, arrangementer osv. Køkkenerne kom op, der blev sat et musikanlæg op og duften af hjemmelavet, autentisk mad begyndte at brede sig.</p>
			<p>Dørene blev slået op den 19. august 2016. Det er uhøjtideligt, ærligt, råt og hamrende velsmagende - Det er Aarhus Street Food.</p>
			<p>Og det er også i regnvejr!</p>
		</article>
	
		
