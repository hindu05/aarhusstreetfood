			<p>The Mexican Donkey har rødder i den Mexicanske jord. Vores kærlighed til Mexicansk streetfood
stammer fra Isabels familie som er mexicanere. Fabiola har lært os alle opskrifter, og Isabel er
opvokset med chili, tacos og masser af gode madoplevelser, som det mexicanske køkken netop
kan give. Vi lever og ånder for den autentiske og originale street food fra Mexico og importerer
selv de råvarer vi har brug for, samtidig er alt vores tilbehør er hjemmelavet. </p>
			<p>Vores mad er yderst velegnet til vegetarer og veganere, den er sund og tilberedt fra bunden af. 
Vores tostadas og tacoer er glutenfri og lavet af 100 % majsmel, og er derfor velegnede til glutenallergikere.
Retter: Vi laver taco, tostadas, burrito og tortas (spicy mexicansk sandwich)</p>
			<p>Kød: Hvidløg/lime marineret oksekødsstrimler. Kylling i mole rojo samt gryderetter til vinter</p>
			<p>Tilbehør: Hjemmelavet bønnemos, rødkålssalat, chilisalsa, hjemmesyltede rødløg, hjemmerørt
